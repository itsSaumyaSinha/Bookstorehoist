import React from "react";
import "slick-carousel/slick/slick.css";
import "slick-carousel/slick/slick-theme.css";
import Slider from "react-slick";
import list from "../../public/list.json";
import Cards from "./Cards"; 

function Freebook() {
  const filterData = list.filter((data) => data.category === "Fiction");
  
  var settings = {
    dots: true,
    infinite: false,
    speed: 500,
    slidesToShow: 3,
    slidesToScroll: 3,
    initialSlide: 0,
    responsive: [
      {
        breakpoint: 1024,
        settings: {
          slidesToShow: 3,
          slidesToScroll: 3,
          infinite: true,
          dots: true,
        }
      },
      {
        breakpoint: 600,
        settings: {
          slidesToShow: 2,
          slidesToScroll: 2,
          initialSlide: 2,
        }
      },
      {
        breakpoint: 480,
        settings: {
          slidesToShow: 1,
          slidesToScroll: 1,
        },
      },
    ],
  };

  return (
    <>
      <div className=" ml-15 max-w-screen-2xl container mx-auto md:px-20 px-4">
        <div>
          <h1 className=" font-semibold text-2xl pb-2">Featured Fiction Picks</h1>
          <p>
          Dive into our handpicked collection of popular fiction novels that are captivating readers worldwide. From thrilling mysteries to heartwarming romances, explore stories that will keep you hooked from the first page to the last.
          <br/>
          One more Page!!!
          </p>
        </div>
        <div>
          <Slider {...settings}>
            {filterData.map((item) => (
              <Cards item={item} key={item.id} />
            ))}
          </Slider>
        </div>
      </div>
    </>
  );
}

export default Freebook;
